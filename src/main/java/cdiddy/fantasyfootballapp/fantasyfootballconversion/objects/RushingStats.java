package cdiddy.fantasyfootballapp.fantasyfootballconversion.objects;

/**
 *
 * @author cedric
 */
public class RushingStats 
{
    private String name;
    private String att;
    private String yds;
    private String tds;
    private String lng;
    private String twopta;
    private String twoptm;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAtt() {
        return att;
    }

    public void setAtt(String att) {
        this.att = att;
    }

    public String getYds() {
        return yds;
    }

    public void setYds(String yds) {
        this.yds = yds;
    }

    public String getTds() {
        return tds;
    }

    public void setTds(String tds) {
        this.tds = tds;
    }

    public String getLng() {
        return lng;
    }

    public void setLng(String lng) {
        this.lng = lng;
    }

    public String getTwopta() {
        return twopta;
    }

    public void setTwopta(String twopta) {
        this.twopta = twopta;
    }

    public String getTwoptm() {
        return twoptm;
    }

    public void setTwoptm(String twoptm) {
        this.twoptm = twoptm;
    }
    
    
    
}
