package cdiddy.fantasyfootballapp.fantasyfootballconversion.objects;

/**
 *
 * @author cedric
 */
public class FumblesStats 
{
    private String name;
    private String tot;
    private String rcv;
    private String trcv;
    private String yds;
    private String lost;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getTot() {
        return tot;
    }

    public void setTot(String tot) {
        this.tot = tot;
    }

    public String getRcv() {
        return rcv;
    }

    public void setRcv(String rcv) {
        this.rcv = rcv;
    }

    public String getTrcv() {
        return trcv;
    }

    public void setTrcv(String trcv) {
        this.trcv = trcv;
    }

    public String getYds() {
        return yds;
    }

    public void setYds(String yds) {
        this.yds = yds;
    }

    public String getLost() {
        return lost;
    }

    public void setLost(String lost) {
        this.lost = lost;
    }
    
    
}
