package cdiddy.fantasyfootballapp.fantasyfootballconversion.objects;

/**
 *
 * @author cedric
 */
public class PuntingStats 
{
    private String name;
    private String pts;
    private String yds;
    private String avg;
    private String i20;
    private String lng;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPts() {
        return pts;
    }

    public void setPts(String pts) {
        this.pts = pts;
    }

    public String getYds() {
        return yds;
    }

    public void setYds(String yds) {
        this.yds = yds;
    }

    public String getAvg() {
        return avg;
    }

    public void setAvg(String avg) {
        this.avg = avg;
    }

    public String getI20() {
        return i20;
    }

    public void setI20(String i20) {
        this.i20 = i20;
    }

    public String getLng() {
        return lng;
    }

    public void setLng(String lng) {
        this.lng = lng;
    }
    
    
    
    
    
}
