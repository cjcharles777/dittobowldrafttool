package cdiddy.util.fantasyfootballconversion.objects;

/**
 *
 * @author cedric
 */
public class NFLPlayer 
{
        private String status; 
        private String weight;
        private String number; 
        private String height;
        private String college;
        private String name;
        private Integer years_pro;
        private String birthdate;
        private String gsisid;
        private String team; 
        private String position; 
        private String profile_url;

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getWeight() {
        return weight;
    }

    public void setWeight(String weight) {
        this.weight = weight;
    }

    public String getNumber() {
        return number;
    }

    public void setNumber(String number) {
        this.number = number;
    }

    public String getHeight() {
        return height;
    }

    public void setHeight(String height) {
        this.height = height;
    }

    public String getCollege() {
        return college;
    }

    public void setCollege(String college) {
        this.college = college;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Integer getYears_pro() {
        return years_pro;
    }

    public void setYears_pro(Integer years_pro) {
        this.years_pro = years_pro;
    }

    public String getBirthdate() {
        return birthdate;
    }

    public void setBirthdate(String birthdate) {
        this.birthdate = birthdate;
    }

    public String getGsisid() {
        return gsisid;
    }

    public void setGsisid(String gsisid) {
        this.gsisid = gsisid;
    }

    public String getTeam() {
        return team;
    }

    public void setTeam(String team) {
        this.team = team;
    }

    public String getPosition() {
        return position;
    }

    public void setPosition(String position) {
        this.position = position;
    }

    public String getProfile_url() {
        return profile_url;
    }

    public void setProfile_url(String profile_url) {
        this.profile_url = profile_url;
    }
         
         
}
